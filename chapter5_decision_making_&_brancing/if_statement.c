/*

if Statement : Used for two-way decision making.

Syntax:
if (condition)
    statement;

If condition is true : statement executes

If false : skipped

| Value    | Meaning |
| -------- | ------- |
| `0`      | False   |
| Non-zero | True    |



Forms of if :

1. Simple if
2. if...else
3. Nested if...else
4. else if ladder
*/